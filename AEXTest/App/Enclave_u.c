static const struct {
	size_t nr_ocall;
	void * table[13];
} ocall_table_Enclave = {
	13,
	{
		(void*)Enclave_ocall_print_string,
		(void*)Enclave_readTSC,
		(void*)Enclave_set_affinity,
		(void*)Enclave_ocall_sleep,
		(void*)Enclave_see_pid,
		(void*)Enclave_sgx_oc_cpuidex,
		(void*)Enclave_sgx_thread_wait_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_set_untrusted_event_ocall,
		(void*)Enclave_sgx_thread_setwait_untrusted_events_ocall,
		(void*)Enclave_sgx_thread_set_multiple_untrusted_events_ocall,
		(void*)Enclave_pthread_wait_timeout_ocall,
		(void*)Enclave_pthread_create_ocall,
		(void*)Enclave_pthread_wakeup_ocall,
	}
};
sgx_status_t main_thread(sgx_enclave_id_t eid, int sleep_time, int core_id, int set_aff, int sleep_inside_enclave)
{
	sgx_status_t status;
	ms_main_thread_t ms;
	ms.ms_sleep_time = sleep_time;
	ms.ms_core_id = core_id;
	ms.ms_set_aff = set_aff;
	ms.ms_sleep_inside_enclave = sleep_inside_enclave;
	status = sgx_ecall_switchless(eid, 0, &ocall_table_Enclave, &ms);
	return status;
}

sgx_status_t countADD(sgx_enclave_id_t eid)
{
	sgx_status_t status;
	status = sgx_ecall_switchless(eid, 1, &ocall_table_Enclave, NULL);
	return status;
}

sgx_status_t sl_init_switchless(sgx_enclave_id_t eid, sgx_status_t* retval, void* sl_data)
{
	sgx_status_t status;
	ms_sl_init_switchless_t ms;
	ms.ms_sl_data = sl_data;
	status = sgx_ecall(eid, 2, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

sgx_status_t sl_run_switchless_tworker(sgx_enclave_id_t eid, sgx_status_t* retval)
{
	sgx_status_t status;
	ms_sl_run_switchless_tworker_t ms;
	status = sgx_ecall(eid, 3, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

